            <p>
                <b>What's Sunday Service like?</b>
                <br>
                Our worship is a mixture of contemporary and traditional liturgies. We celebrate the Eucharist every first Sunday. 
                We expect and love your presence here! You can expect to be greeted by friendly people as soon as you arrive. After worship, we hope that you would hang out with us for further conversations. 
            </p>
            <p>
                <b>What's Friday Gathering like?</b>
                <br>
                Too tired to cook food on Friday night? No worries! Friday gatherings begin with dinner at 7:30pm. After we are physically fed, we move to the main sanctuary to be spiritually fed through praise and prayer. Then, we transition to Bible Study or special events in the fellowship hall. 
            </p>
            <p>
                <b>What's Small Group Meetings like?</b>
                <br>
                Small group meetings provide personalized pastoral care. Meetings happen during weekday evenings. They typically begin with dinner, prayers, and discuss spiritual matters that sustain each member during the week. 
                It is low key and welcoming! Each group meets at the small group leader's home or at a restaurant. So, bring your tired bodies and souls. Rest in God and in each other's presence!
            </p>
            <p>
                <b>Am I expected to put money in the offering?</b>
                <br>
                If you are visiting with us, we do not expect you to put money in the offering, and you are welcome to just let it pass you by. If you consider Aletheia Ministries as your home church, we encourage you to share your blessings prayerfully. 
            </p>
            <p>
                <b>Are you affiliated?</b>
                <br>
